

CREATE VIEW [dbo].[vwCP_PATIENT_PATIENT_FLAG]
AS
SELECT DISTINCT 
						PATIENT_FLAG.PatientFlagId, dbo.PATIENT_FLAG.PatientId, dbo.PATIENT.FullName, dbo.PATIENT_FLAG.FlagId, 
						PATIENT_FLAG.SourceSystemId, dbo.PATIENT_FLAG.Tracked, dbo.PRIMARY_CARE_MANAGER.ProviderId
FROM					PATIENT INNER JOIN
                      dbo.PATIENT_FLAG ON dbo.PATIENT.PatientId = dbo.PATIENT_FLAG.PatientId INNER JOIN
                      dbo.PRIMARY_CARE_MANAGER ON dbo.PATIENT.PatientId = dbo.PRIMARY_CARE_MANAGER.PatientID
WHERE     (dbo.PATIENT.CurrentPCMId IS NOT NULL)
AND			PATIENT.PatientId NOT IN (SELECT PATIENTID FROM CLINICAL_PORTAL.DBO.CUSTOM_PATIENT_TRACKING)



