CREATE PROCEDURE [dbo].[upCreateIndexes_LAB_RESULT] AS
	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'ind_LAB_RESULT')
		DROP INDEX LAB_RESULT.ind_LAB_RESULT
	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT_PATIENT_IEN')
		DROP INDEX LAB_RESULT.IX_LAB_RESULT_PATIENT_IEN
		
	CREATE INDEX 	IX_LAB_RESULT_PATIENT_IEN
	ON 			LAB_RESULT(PATIENT_IEN)
	WITH 			FILLFACTOR = 100
				
	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT_KEY_LAB_RESULT')
		DROP INDEX LAB_RESULT.IX_LAB_RESULT_KEY_LAB_RESULT
		
	CREATE INDEX 	IX_LAB_RESULT_KEY_LAB_RESULT
	ON 			LAB_RESULT(KEY_LAB_RESULT)
	WITH 			FILLFACTOR = 100
