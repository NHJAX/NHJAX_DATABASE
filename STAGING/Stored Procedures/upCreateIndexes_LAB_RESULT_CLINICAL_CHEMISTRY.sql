CREATE PROCEDURE [dbo].[upCreateIndexes_LAB_RESULT$CLINICAL_CHEMISTRY] AS
	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'ind_LAB_RESULT$CLINICAL_CHEMISTRY')
		DROP INDEX LAB_RESULT$CLINICAL_CHEMISTRY.ind_LAB_RESULT$CLINICAL_CHEMISTRY

	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT$CLINICAL_CHEMISTRY_KEY_LAB_RESULT')
		DROP INDEX LAB_RESULT$CLINICAL_CHEMISTRY.IX_LAB_RESULT$CLINICAL_CHEMISTRY_KEY_LAB_RESULT
		
	CREATE INDEX 	IX_LAB_RESULT$CLINICAL_CHEMISTRY_KEY_LAB_RESULT
	ON 			LAB_RESULT$CLINICAL_CHEMISTRY(KEY_LAB_RESULT)
	WITH 			FILLFACTOR = 100

	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT$CLINICAL_CHEMISTRY_KEY_LAB_RESULT$CLINICAL_CHEMISTRY')
		DROP INDEX LAB_RESULT$CLINICAL_CHEMISTRY.IX_LAB_RESULT$CLINICAL_CHEMISTRY_KEY_LAB_RESULT$CLINICAL_CHEMISTRY
		
	CREATE INDEX 	IX_LAB_RESULT$CLINICAL_CHEMISTRY_KEY_LAB_RESULT$CLINICAL_CHEMISTRY
	ON 			LAB_RESULT$CLINICAL_CHEMISTRY(KEY_LAB_RESULT$CLINICAL_CHEMISTRY)
	WITH 			FILLFACTOR = 100

	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT$CLINICAL_CHEMISTRY_DATE_TIME_SPECIMEN_TAKEN')
		DROP INDEX LAB_RESULT$CLINICAL_CHEMISTRY.IX_LAB_RESULT$CLINICAL_CHEMISTRY_DATE_TIME_SPECIMEN_TAKEN
		
	CREATE INDEX 	IX_LAB_RESULT$CLINICAL_CHEMISTRY_DATE_TIME_SPECIMEN_TAKEN
	ON 			LAB_RESULT$CLINICAL_CHEMISTRY(DATE_TIME_SPECIMEN_TAKEN)
	WITH 			FILLFACTOR = 100
	
	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT$CLINICAL_CHEMISTRY_LAB_WORK_ELEMENT_IEN')
		DROP INDEX LAB_RESULT$CLINICAL_CHEMISTRY.IX_LAB_RESULT$CLINICAL_CHEMISTRY_LAB_WORK_ELEMENT_IEN
		
	CREATE INDEX 	IX_LAB_RESULT$CLINICAL_CHEMISTRY_LAB_WORK_ELEMENT_IEN
	ON 			LAB_RESULT$CLINICAL_CHEMISTRY(LAB_WORK_ELEMENT_IEN)
	WITH 			FILLFACTOR = 100

	IF EXISTS(SELECT name FROM sysindexes WHERE name = 'IX_LAB_RESULT$CLINICAL_CHEMISTRY_MULTI_KEY')
		DROP INDEX LAB_RESULT$CLINICAL_CHEMISTRY.IX_LAB_RESULT$CLINICAL_CHEMISTRY_MULTI_KEY
		
	CREATE UNIQUE CLUSTERED INDEX 	IX_LAB_RESULT$CLINICAL_CHEMISTRY_MULTI_KEY
	ON 			LAB_RESULT$CLINICAL_CHEMISTRY(KEY_LAB_RESULT,KEY_LAB_RESULT$CLINICAL_CHEMISTRY)
	WITH 			FILLFACTOR = 100
